# 영문 대문자를 입력한다
string = input()

# S인 경우
if string == 'S':
    print("Superior")

# A인 경우
elif string == 'A':
    print("Excellent")

# B인 경우
elif string == 'B':
    print("Good")

# C인 경우
elif string == 'C':
    print("Usually")

# D인 경우
elif string == 'D':
    print("Effort")

# 그 이외의 경우
else:
    print("Failure")